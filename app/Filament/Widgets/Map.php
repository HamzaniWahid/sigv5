    public function getMarkers(): array
    {
        // Mengambil semua data sekolah dari database
        $sekolahData = Sekolah::all();

        // Membuat array marker berdasarkan data sekolah
        $markers = [];
        foreach ($sekolahData as $sekolah) {
            // Memisahkan latitude dan longitude
            $location = explode(',', $sekolah->lokasi_latlng);

            // Memastikan lokasi memiliki dua elemen (latitude dan longitude)
            if (count($location) === 2) {
                $lat = $location[0];
                $lng = $location[1];

                $markers[] = Marker::make($sekolah->id)
                    ->lat($lat)
                    ->lng($lng)
                    ->tooltip($sekolah->nama . '<br>' . $sekolah->alamat . '<br><a href="' . $sekolah->web . '" target="_blank">' . $sekolah->web . '</a>');
            }
        }

        return $markers;
    }
    public function getPolygones(): array
    {
        // Mengambil semua data sekolah dari database
        $wilayahs = Wilayah::all();

        // Membuat array marker berdasarkan data sekolah
        $polygones = [];
        foreach ($wilayahs as $wilayah) {
            // Hapus tanda petik dari string
            $cleanString = str_replace('"', '', $wilayah->polygon_latlng);

            // Pecah string menjadi array
            $latlngArray = preg_split('/\],\[\s*/', trim($cleanString, '[]'));

            // Ubah elemen menjadi array koordinat
            $coordinates = array_map(function ($coords) {
                return array_map('floatval', explode(',', $coords));
            }, $latlngArray);
            // var_dump($coordinates);
            // print_r($latlang);
            // var_dump($latlang);
            $polygones[] = Polygone::make($wilayah->nama)
                ->latlngs($coordinates)
                ->options(['color' => $wilayah->warna])
                ->tooltip($wilayah->nama);
            // }
        }
        // dd($polygones);

        return $polygones;
    }




